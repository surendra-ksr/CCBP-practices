class Gallery extends Component {
  state = {selectedImgId: imagesList[0].id}

  onClickThumbnail = id => {
    this.setState({selectedImgId: id})
  }

  render() {
    const {selectedImgId} = this.state
    const sImg = imagesList.filter(eachImg => eachImg.id === selectedImgId)
    const reqImgUrl = sImg[0].imageUrl
    const reqImgAlt = sImg[0].imageAltText

    return (
      <div className="main-div">
        <img src={reqImgUrl} alt={reqImgAlt} />
        <h1 className="main-h1">Nature Photography</h1>
        <p className="main-p">Nature Photography by Rahul</p>
        <ul className="ul-div">
          {imagesList.map(eachImg => (
            <ThumbnailItem
              key={eachImg.id}
              onClickThumbnail={this.onClickThumbnail}
              isActive={eachImg.id === selectedImgId}
              imgDetails={eachImg}
            />
          ))}
        </ul>
      </div>
    )
  }
}

export default Gallery
